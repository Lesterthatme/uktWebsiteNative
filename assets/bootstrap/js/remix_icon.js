document.addEventListener("DOMContentLoaded", function () {
    const iconDropdownMenu = document.getElementById("iconList");

    // Populate the dropdown dynamically
    remixIcons.forEach((icon) => {
        let listItem = document.createElement("li");
        listItem.innerHTML = `
            <button type="button" class="dropdown-item" onclick="selectIcon('${icon}')">
                <i class="${icon}"></i> ${icon.replace("ri-", "").replace("-line", "").replace("-fill", "").toUpperCase()}
            </button>
        `;
        iconDropdownMenu.appendChild(listItem);
    });
});

// Function to select an icon
function selectIcon(iconClass) {
    document.getElementById("selectedIconInput").value = iconClass;
    document.getElementById("selectedIconPreview").className = iconClass;
    document.getElementById("selectedIconName").textContent = iconClass;
}

// Function to filter icons
function filterIcons() {
    let input = document.getElementById("iconSearch").value.toLowerCase();
    let items = document.querySelectorAll("#iconList li");

    items.forEach((item) => {
        let text = item.textContent.toLowerCase();
        item.style.display = text.includes(input) ? "" : "none";
    });
}
